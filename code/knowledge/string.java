package knowledge;

public class string {
    public static void main(String[] args) {
        System.out.println("name" + '\t' + "aaa");
        System.out.println("age" + '\t' + "18\n");
        System.out.println("name" + '\r' + "aaa\n");
        System.out.println("name" + '\n' + "aaa");
    /*
    特殊字符：
    \t   打印时，把那面字符串的长度补齐到8（或8的倍数）
    \r   回车符（删除前面的字符串，同时光标到最前方）
    \n   换行符
     */
    }
}
